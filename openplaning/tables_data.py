def get_table_data(table_name):
    """
    Get table data by name.
    
    Args:
        table_name: Name of the table (e.g., 'Raw_0.2', 'V_0.4', etc.)
    
    Returns:
        numpy.ndarray: Table data array
    """
    tables = {
        'Raw_0.2': RAW_02_DATA,
        'Raw_0.4': RAW_04_DATA,
        'Raw_0.6': RAW_06_DATA,
        'V_0.2': V_02_DATA,
        'V_0.4': V_04_DATA,
        'Raw_V_0.2': RAW_V_02_DATA,
        'Raw_V_0.4': RAW_V_04_DATA,
        'Raw_V_0.6': RAW_V_06_DATA,
    }
    
    if table_name not in tables:
        raise ValueError(f"Unknown table: {table_name}. Available tables: {list(tables.keys())}")
    
    return tables[table_name]


def get_z_max_table():
    """Get z_max interpolation tables."""
    return Z_MAX_BETA_TABLE, Z_MAX_VALUES


def get_z_max_poly():
    """Get z_max polynomial coefficients."""
    return Z_MAX_POLY_COEFFS
